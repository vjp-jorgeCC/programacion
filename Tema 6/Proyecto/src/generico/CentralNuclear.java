    public ArrayList<Celda> getAdyacencia(int fila, int columna){
        ArrayList <Celda> celdasAdyacentes = new ArrayList<>();
        int columnaOeste = columna - 1;
        int columnaEste = columna + 1;
        int filaNorte = fila - 1;
        int filaSur = fila + 1;
        // Este
        if (comprobarMatriz(fila, columnaEste)) {
            Celda celdaEste = getCelda(fila, columnaEste);
            celdasAdyacentes.add(celdaEste);
        }
                
        //Oeste
        if (comprobarMatriz(fila, columnaOeste)) {
            Celda celdaOeste = getCelda(fila, columnaOeste);
        celdasAdyacentes.add(celdaOeste);
        }        
        
        //Norte
        if (comprobarMatriz(filaNorte, columna)) {
            Celda celdaNorte = getCelda(filaNorte, columna);
            celdasAdyacentes.add(celdaNorte);   
        }
        
        // Sur
        if (comprobarMatriz(filaSur, columna)) {
            Celda celdaSur = getCelda(filaSur, columna);
            celdasAdyacentes.add(celdaSur);
        }        
        
        // NorEste
        if (comprobarMatriz(filaNorte, columnaEste)) {
            Celda celdaNorEste = getCelda(filaNorte, columnaEste);
            celdasAdyacentes.add(celdaNorEste);
        }
        
        // SurEste
        if (comprobarMatriz(filaSur, columnaEste)) {
            Celda celdaSurEste = getCelda(filaSur, columnaEste);
            celdasAdyacentes.add(celdaSurEste);
        }        
        
        // NorOeste
        if (comprobarMatriz(filaNorte, columnaOeste)) {
            Celda celdaNorOeste = getCelda(filaNorte, columnaOeste);
            celdasAdyacentes.add(celdaNorOeste);
        }        
        
        // SurOeste
        if (comprobarMatriz(filaSur, columnaOeste)) {
            Celda celdaSurOeste = getCelda(filaSur, columnaOeste);
            celdasAdyacentes.add(celdaSurOeste);
        }
        
        // Retornar el ArrayList
        return celdasAdyacentes;
    }
